// Toggle the menu visibility
function toggleLangMenu() {
    document.getElementById('langOptions').classList.toggle('show');
}

// Close menu if user clicks outside
window.onclick = function(event) {
    if (!event.target.closest('.custom-lang-dropdown')) {
        document.getElementById('langOptions').classList.remove('show');
    }
}

function changeLanguage(lang) {
    localStorage.setItem("userLanguage", lang);
    document.documentElement.lang = lang;
    
    // Update the trigger label
    document.getElementById('current-lang-label').innerText = (lang === 'am') ? 'áŠ áˆ›áˆ­áŠ›' : 'English';

    // Update all elements with data-key
    const elements = document.querySelectorAll("[data-key]");
    elements.forEach(el => {
        const key = el.getAttribute("data-key");
        if (ebroTranslations[lang] && ebroTranslations[lang][key]) {
            el.innerText = ebroTranslations[lang][key];
        }
    });
    
    // Hide menu after selection
    document.getElementById('langOptions').classList.remove('show');
}

// Initialize on page load
window.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem("userLanguage");
    if (savedLang) {
        changeLanguage(savedLang);
    }
});

/*account page*/
function showDeleteNotice() {
    const lang = localStorage.getItem("userLanguage") || "en";
    const message = ebroTranslations[lang]["delete_error"];
    if (message) {
        alert(message);
    } else {
        alert("Notice: You cannot delete this order.");
    }
}

function confirmDelete(event, url) {
    event.preventDefault();
    const lang = localStorage.getItem("userLanguage") || "en";
    const message = ebroTranslations[lang]["confirm_delete"];
    if (confirm(message)) {
        window.location.href = url;
    }
    return false;
}

function processLogout() {
    // 1. Check current language
    const lang = localStorage.getItem("userLanguage") || "en";
    
    // 2. Get message from dictionary
    const msg = ebroTranslations[lang]["logout_confirm"] || "Are you sure you want to log out?";

    // 3. Show confirmation
    if (confirm(msg)) {
        window.location.href = 'logout.php';
    }
}